public class Calculadora {

    double soma(double a,double b){
        return(a+b);
    }
    double subtração(double a,double b){
        return(a-b);
    }

    double divisão(double a,double b){
        return(a/b);
    }

    double multiplicação(double a,double b){
        return(a*b);
    }

}
